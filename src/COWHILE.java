public class COWHILE {

    public static void main(String[] args){

        //while
        int numeroWhile = 1;

        while(numeroWhile < 3){
            numeroWhile++;
            System.out.println(numeroWhile);

        }

        //do while

        int numerodoWhile = 3;
        do {
            numerodoWhile++;
            System.out.println("La variable numero es: "+ numeroWhile );
        }while (numeroWhile < 3);


    }

}
